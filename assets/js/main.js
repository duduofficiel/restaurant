const foodItems = [
  "Pizza", "Pâtes", "Gâteaur", "Salade", "Bifteck", "Taco", "Sushi", 
  "Sandwich", "Beignets", "Glace"
];

// ველის ინიციალიზაცია
const searchBox = document.getElementById('search-box');
const searchForm = document.querySelector('.searchform');

// ეძებს შესაბამის საჭმელს
function searchSuggestions() {
  const query = searchBox.value.toLowerCase();
  const suggestions = foodItems.filter(item => 
      item.toLowerCase().includes(query)
  );

  displaySuggestions(suggestions);
}

// აჩვენებს წინადადებებს
function displaySuggestions(suggestions) {
  // წაშლის ძველ წინადადებებს
  let suggestionBox = document.querySelector('.suggestions-box');
  if (suggestionBox) {
      suggestionBox.remove();
  }

  // თუ არ არსებობს, ქმნის ახალ ელემენტს წინადადებებისთვის
  suggestionBox = document.createElement('div');
  suggestionBox.className = 'suggestions-box';
  searchForm.appendChild(suggestionBox);

  // თუ შედეგები არის, აჩვენებს შესაბამის ელემენტებს
  if (suggestions.length > 0) {
      suggestions.forEach(suggestion => {
          const suggestionItem = document.createElement('div');
          suggestionItem.className = 'suggestion-item';
          suggestionItem.textContent = suggestion;
          suggestionItem.addEventListener('click', function() {
              searchBox.value = suggestion;
              suggestionBox.remove();
          });
          suggestionBox.appendChild(suggestionItem);
      });
  } else {
      suggestionBox.innerHTML = '<div class="suggestion-item">No results found</div>';
  }
}

// აკონტროლებს შეყვანილ ტექსტს
searchBox.addEventListener('input', searchSuggestions);



